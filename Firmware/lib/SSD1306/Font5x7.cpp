const uint8_t* Font5x7::getCharData(char c){
  if(c < Font5x7::data5x7Start || c > Font5x7::data5x7End){
    // if out of bound return a .
    return Font5x7::data5x7;
  }

  uint8_t offset = pgm_read_byte(Font5x7::data5x7StartAddr + (c - '.'));
  return Font5x7::data5x7 + offset;
}

const uint8_t Font5x7::getCharWidth(char c){
  if(c < Font5x7::data5x7Start || c > Font5x7::data5x7End){
    // if out of bound return a .
    return 1;
  }

  switch(c){
    case '.':
      return 1;
    default:
      return 5;
  }
}
